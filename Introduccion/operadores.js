// Operadores aritmeticos
let num1 = 10
let num2 = 5
let resultado = num1 + num2 // Suma
console.log(resultado) // Imprime 15

// Operadores de comparación
let esMayor = num1 > num2 // Comparación
console.log(esMayor) // Imprime true

// Operadores de asignacion
let a = 10
let b = "10"

let esIgual = a !== b
console.log(esIgual) //false


// Operador ternario
let mensaje = (esMayor) ? "El primer número es mayor" : "El segundo número es mayor o igual"
console.log(mensaje)

